package myfirst_codeOperotors;

public class Shiftedoperators {

	public static void main(String[] args) {
		/* shifted operators are two types
		 1 left shifted
		 2 right shifted
		 */
		System.out.println(10<<4);
		System.out.println(10>>4);
		System.out.println(10<<8);
		System.out.println(10>>6);
		

	}

}
